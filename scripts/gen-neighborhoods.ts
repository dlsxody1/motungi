const SIDO = "서울특별시";

/** SQL 문자열 리터럴 이스케이프 (single-quote 이중화). */
function sq(s: string): string {
  return "'" + s.replace(/'/g, "''") + "'";
}

/** (구|동) 조합의 안정적 대체키. 코드 미확보 동은 이 slug를 PK로 사용. */
function admCode(gu: string, dong: string): string {
  return `SEO-${gu}-${dong}`;
}

type Row = {
  admCode: string;
  sigungu: string;
  dong: string;
  lat: number;
  lng: number;
  coordLevel: "seed" | "sigungu";
};

function buildRows(): Row[] {
  const rows: Row[] = [];
  for (const gu of Object.keys(DONGS)) {
    const center = GU_CENTER[gu];
    if (!center) throw new Error(`구 중심좌표 없음: ${gu}`);
    for (const dong of DONGS[gu]) {
      const overrideKey = `${gu}|${dong}`;
      const override = SEED_OVERRIDES[overrideKey];
      rows.push({
        admCode: admCode(gu, dong),
        sigungu: gu,
        dong,
        lat: override ? override.lat : center.lat,
        lng: override ? override.lng : center.lng,
        coordLevel: override ? "seed" : "sigungu",
      });
    }
  }
  return rows;
}

